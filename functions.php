function rtPhotographyTheme_enqueue_scripts(){
    wp_enqueue_style('fontawsome-style','https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.3.0/css/all.min.css',);
    wp_enqueue_style('boxIcon-style','https://unpkg.com/boxicons@2.1.4/css/boxicons.min.css',);
